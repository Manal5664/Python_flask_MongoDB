### Arithmetic Operations

# Python supports various arithmetic operations. 
# Assume 'num1' and 'num2' from earlier examples:
num1 = 30
num2 = 19.99

# Addition
sum_num1_num2 = num1 + num2
print(f"num1 + num2: {sum_num1_num2}")

# Subtraction
diff_num2_num1 = num2 - num1
print(f"num2 - num1: {diff_num2_num1}")

# Multiplication
total_cost = num2 * 2
print(f"num2 * 2: {total_cost}")

# Division (results in a float)
divided_num1 = num1 / 2
print(f"num1 / 2: {divided_num1}")

# Floor Division (results in an integer, discards remainder)
floor_divided_num1 = num1 // 2
print(f"num1 // 2: {floor_divided_num1}")

# Modulo (returns the remainder of a division)
remainder = num1 % 4
print(f"num1 % 4: {remainder}")

# Exponentiation
power_example = 2 ** 3
print(f"2 to the power of 3: {power_example}")


